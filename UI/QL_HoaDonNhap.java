    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDonNhap.getModel();

        // xóa dữ liệu trên table
        model.setRowCount(0);
        if (true) {
            try {
                List<HoaDonNhap> list = dao.selectAll();//Đọc dữ liệu từ CSDL
                for (HoaDonNhap entity : list) {
                    Object[] row = {
                        entity.getMaNhapHang(),
                        entity.getMaSanPham(),
                        entity.getSoLuong(),
                        entity.getGiaTien(),
                        entity.getMaquanly(),
                        DateHelper.toString(entity.getNgayNhap()),
                        entity.getGhiChu(),
                        entity.getMaCuaHang()
                    };
                    model.addRow(row);

                }
            } catch (Exception e) {
                MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            }
        }
    }

    void fillCombobox() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboTenSanPham.getModel();

        model.removeAllElements();
        List<SanPham> list = spDao.selectAll();
        model.addElement(new SanPham("Mã SP", "Tên SP", "Nhà SX", ""));
        for (SanPham sanPham : list) {
            model.addElement(sanPham);
        }
    }

    void setForm(HoaDonNhap hd) {
this.resetBorder();
        txtMaNhapHang.setText(hd.getMaNhapHang());
        txtSoLuong.setText(hd.getSoLuong() + "");
        txtGhiChu.setText(hd.getGhiChu());
        txtGiaTien.setText(hd.getGiaTien() + "");
        txtMaQuanLy.setText(hd.getMaquanly());
        lblNgayNhap.setText(DateHelper.toString(hd.getNgayNhap()));
//        txtMaCuaHang.setText(hd.getMaCuaHang());
        cboMaCuaHang.setSelectedIndex(0);
        lblMaSanPham.setText(hd.getMaSanPham());

        SanPham sp = spDao.selectById(hd.getMaSanPham());

//        System.out.println(hd.getMaSanPham());
//        String a = sp.toString();
        if (sp != null) {
            cboTenSanPham.setSelectedItem(sp);
        } else {
            cboTenSanPham.setSelectedIndex(0);
        }
    }

    void updateStatus() {
        boolean edit = row >= 0;
        boolean first = row == 0;
        boolean last = row == tblHoaDonNhap.getRowCount() - 1;
        //trạng thái form
        txtMaNhapHang.setEditable(!edit);//vì mặc đinh row = -1
        btnThem.setEnabled(!edit);

        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        //trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
        
        DesignHelper.setButtonBackgroundColor(btnThem, new Color(51, 255, 0), new Color(102, 255, 102));
        DesignHelper.setButtonBackgroundColor(btnXoa, new Color(255, 0, 0), new Color(255, 102, 102));
        DesignHelper.setButtonBackgroundColor(btnSua, new Color(255, 255, 0), new Color(255, 255, 153));

        // nút điều khiển
        DesignHelper.setButtonBackgroundColor(new Color(153, 204, 255), new Color(204, 255, 255),
                btnFirst, btnPrev, btnNext, btnLast);
    }

