    void fillToJlist() {
        DefaultListModel listModel = new DefaultListModel();
        jlistmasp.removeAll();
        List<SanPham> listsp = sanPhamDao.selectAll();
        listModel.addElement("Tất cả");
        for (SanPham sp : listsp) {
            listModel.addElement(sp.getMaSanPham());
        }
        jlistmasp.setModel(listModel);
    }

    void fillToComBoboxthang() {
        DefaultComboBoxModel cbomodel = new DefaultComboBoxModel();
        cbothang.removeAll();
        cbomodel.addElement(" Tất cả Tháng ");
        for (int i = 1; i < 13; i++) {
            cbomodel.addElement("Tháng " + i);
        }
        cbothang.setModel(cbomodel);
    }

    void fillToComBoBoxNam() {
        DefaultComboBoxModel cbomodel = new DefaultComboBoxModel();
        cbonam.removeAll();
        Date date = new Date();

        simpleDateFormat.applyPattern("yyyy");
        String format = simpleDateFormat.format(date);
        int nam = Integer.parseInt(format);
        cbomodel.addElement(" Tất cả năm ");
        for (int i = 2020; i <= nam; i++) {
            cbomodel.addElement("Năm " + i);
        }
        cbonam.setModel(cbomodel);
    }

    private void fillToTable(int n) {
        DefaultTableModel model = (DefaultTableModel) tblsanpham.getModel();
        model.setRowCount(0);
        String thang = null;
        String thang1;
        String nam1;
        if (cbothang.getSelectedIndex() == 0) {
            thang = "";
            thang1 = " Tất cả tháng";
        } else if (cbothang.getSelectedIndex() == 1 || cbothang.getSelectedIndex() == 2) {
            thang = "0" + String.valueOf(cbothang.getSelectedIndex());
            thang1 = "Tháng " + cbothang.getSelectedIndex();
        } else {
            thang = String.valueOf(cbothang.getSelectedIndex());
            thang1 = "Tháng " + cbothang.getSelectedIndex();
        }
        //lấy năm chọn trên combobox
        if (cbonam.getSelectedIndex() == 0) {
            nam = "";
            nam1 = "Tất cả năm";
        } else {
            nam = (String) cbonam.getSelectedItem();
            nam = nam.substring(4);
            nam1 = (String) cbonam.getSelectedItem();
        }
        //lấy sản phẩm chọn trên jlist
        String masp = jlistmasp.getSelectedValue();
        if (jlistmasp.getSelectedIndex() == 0) {
            masp = "";
        }
        List<Object[]> listdoanhthu;
        if (n == 0) {
            listdoanhthu = thongkedao.getThongKeSanPhamBanNhieu(thang, nam, masp);
            for (Object[] row : listdoanhthu) {
                model.addRow(new Object[]{row[0], row[1], row[2], row[3], row[4]});
            }
        } else {
            listdoanhthu = thongkedao.getThongKeSanPhamHetHoacNhoHon10(thang, nam, masp, n);
            for (Object[] row : listdoanhthu) {
                model.addRow(new Object[]{row[0], row[1], row[2], row[3], row[4]});
            }
        }
        lbltieude.setText("Số Lượt bán của sản phẩm " + masp + " " + nam1 + " " + thang1);//set thanh tieu de
    }

