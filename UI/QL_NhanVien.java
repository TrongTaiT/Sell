    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();

        // xóa dữ liệu trên table
        model.setRowCount(0);
        if (true) {
            try {
                String keyword = txtTimKiem.getText();
                List<NhanVien> list = dao.selectByKeyWord(keyword);//Đọc dữ liệu từ CSDL
                for (NhanVien entity : list) {
                    String vaiTro;
                    switch (entity.getVaiTro()) {
                        case 1:
                            vaiTro = "Nhân Viên";
                            break;
                        case 2:
                            vaiTro = "Quản Lý";
                            break;
                        case 3:
                            vaiTro = "Giám Đốc";
                            break;
                        default:
                            vaiTro = "Đã Nghỉ Việc";
                            break;
                    }
                    Object[] rowData = {
                        entity.getMaNV(),
                        entity.getHoTen(),
                        entity.getGioiTinh() ? "Nam" : "Nữ",
                        DateHelper.toString(entity.getNgaySinh()),
                        entity.getDienThoai(),
                        //                        String.valueOf(entity.getLuong()),
                        String.format("%.2f", entity.getLuong()),
                        entity.getEmail(),
                        vaiTro,
                        entity.getDiaChi(),
                        entity.getMaCuaHang()
                    };
                    model.addRow(rowData);

                }
            } catch (Exception e) {
                System.out.println(e + " loi dong 50*");
            }
        }
    }

