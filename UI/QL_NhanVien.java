    void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtNgaySinh.setDate(nv.getNgaySinh());
        txtDiaChi.setText(nv.getDiaChi());
        txtDienThoai.setText(nv.getDienThoai());
        txtLuong.setText(String.format("%.2f", nv.getLuong()));
        txtEmail.setText(nv.getEmail());
        String vaiTro;
        switch (nv.getVaiTro()) {
            case 1:
                vaiTro = "Nhân Viên";
                break;
            case 2:
                vaiTro = "Quản Lý";
                break;
            case 3:
                vaiTro = "Giám Đốc";
                break;
            default:
                vaiTro = "Đã Nghỉ Việc";
                break;
        }

        cboVaiTro.setSelectedItem(vaiTro);

        if (nv.getGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

        CuaHang ch = chDao.selectById(nv.getMaCuaHang());
        if (ch != null) {
            cboCuaHang.setSelectedItem(ch);
        }else{
            cboCuaHang.setSelectedIndex(0);
        }
    }

    void updateStatus() {
        boolean edit = row >= 0;
        boolean first = row == 0;
        boolean last = row == tblNhanVien.getRowCount() - 1;
        //trạng thái form
        txtMaNV.setEditable(!edit);//vì mặc đinh row = -1
        btnThem.setEnabled(!edit);

        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        //trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

        // nút trạng thái
        DesignHelper.setButtonBackgroundColor(btnThem, new Color(51, 255, 0), new Color(102, 255, 102));
        DesignHelper.setButtonBackgroundColor(btnXoa, new Color(255, 0, 0), new Color(255, 102, 102));
        DesignHelper.setButtonBackgroundColor(btnSua, new Color(255, 255, 0), new Color(255, 255, 153));

        // nút điều khiển
        DesignHelper.setButtonBackgroundColor(new Color(153, 204, 255), new Color(204, 255, 255),
                btnFirst, btnPrev, btnNext, btnLast);

    }

    NhanVien getForm() {
        NhanVien nv = new NhanVien();
        CuaHang ch = (CuaHang) cboCuaHang.getSelectedItem();

        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        nv.setEmail(txtEmail.getText());
        nv.setDienThoai(txtDienThoai.getText());
        nv.setLuong(Float.parseFloat(txtLuong.getText()));
        nv.setVaiTro(cboVaiTro.getSelectedIndex());
        nv.setGioiTinh(rdoNam.isSelected());
        nv.setNgaySinh(txtNgaySinh.getDate());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setMaCuaHang(ch.getMaCH());
        return nv;
    }

    void clearForm() {
        NhanVien nv = new NhanVien();
        nv.setGioiTinh(true);
        nv.setVaiTro(1);
        nv.setLuong(0f);
        tblNhanVien.clearSelection();
       
        this.setForm(nv);
        this.setTextStartAndNew();
        this.row = -1;
        this.updateStatus();
    }

    void edit() {
        String manv = (String) tblNhanVien.getValueAt(row, 0);
        NhanVien nv = dao.selectById(manv);
        this.setPlaceholderForSetForm();
        this.setForm(nv);
        this.updateStatus();
    }

    void insert() {
        NhanVien nv = getForm();
        nv.setMatKhau("null");
        try {
            dao.insert(nv);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm không thành công!");
        }

    }

    void update() {
        NhanVien nv = getForm();
        try {
            dao.update(nv);
            this.fillTable();
            MsgBox.alert(this, "Cập nhật thành công!");

        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật không thành công!");
        }

    }

    void delete() {
        if (true) {
//            String manv = txtMaNV.getText();
            if (MsgBox.confirm(this, "Bạn có muốn xóa hay không?")) {
                try {
                    String manv1 = txtMaNV.getText();
                    dao.delete(manv1);
                    this.fillTable();
                    this.clearForm();
                    JOptionPane.showMessageDialog(this, "Xoá thành công");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Xoá không thành công");
                }
            }
        }

    }

    void find() {
        this.fillTable();
        this.row = -1;
        this.clearForm();
        this.updateStatus();
    }

    void first() {
        row = 0;
        edit();
    }

    void prev() {
        if (row > 0) {
            row--;
            edit();
        }
    }

    void next() {
        if (row < tblNhanVien.getRowCount() - 1) {
            row++;
            edit();
        }
    }

    void last() {
        row = tblNhanVien.getRowCount() - 1;
        edit();
    }

