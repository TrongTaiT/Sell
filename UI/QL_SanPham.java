    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = spDAO.selectAll();
            for (SanPham sp : list) {
                if (!sp.getMaSanPham().equals("(Mã SP)")) {
                    model.addRow(new Object[]{
                        sp.getMaSanPham(),
                        sp.getTenSP(),
                        sp.getNhaSX(),
                        sp.getGiaTien()

                    });
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

